package EXERCISES.LESSON1A;

public class Special_Strings {
    public static void main(String[] args) {
        System.out.print("To jest nowa linijka\n" + "I newe zdanie");
        System.out.print("\n");
        System.out.print("To jest nowa linijka\t" + "I newe zdanie");
        System.out.print("\n");
        System.out.print("To jest nowa linijka\r" + "I newe zdanie");
        System.out.print("\n");
        System.out.print("To jest nowa linijka\b" + "I newe zdanie");
        System.out.print("\n");
        System.out.print("\'To jest nowa linijka\'" + "\'I newe zdanie\'\n");
        System.out.print("\"To jest nowa linijka\"" + "\"I newe zdanie\"\n");
        System.out.print("\\To jest nowa linijka\\" + "\\I newe zdanie\\\n");
        System.out.println("\124\n");
        System.out.println("\u0121\n");
    }
}
